int		ft_atoi(char *str)
{
	int r;
	int signo;

	r = 0;
	signo = 1;
	while ((*str == ' ') || (*str >= 9 && *str <= 13))
		++str;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			signo *= -1;
		str++;
	}
	while ('0' <= *str && *str <= '9')
	{
		r *= 10;
		r += (signo * (*str - 48));
		str++;
	}
	return (r);
}

int		ft_is_prime(int nb)
{
	int cont1;

	if (nb < 2)
		return (0);
	cont1 = 2;
	while (cont1 <= nb / cont1)
	{
		if (nb % cont1 == 0)
			return (0);
		cont1++;
	}
	return (1);
}

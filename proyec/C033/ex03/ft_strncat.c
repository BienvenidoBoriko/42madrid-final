char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char	*c_s1;

	c_s1 = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0' && nb > 0)
	{
		*dest = *src;
		dest++;
		src++;
		nb--;
	}
	*dest = '\0';
	return (c_s1);
}

#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
	write(1, "\n", 1);
}

int		main(int argc, char **argv)
{
	int		cont;

	cont = 1;
	argc = 0;
	while (argv[cont])
	{
		ft_putstr(argv[cont]);
		cont++;
	}
}

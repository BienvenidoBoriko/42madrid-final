char	*ft_strcat(char *dest, char *src)
{
	char *c_s1;

	c_s1 = dest;
	while (*dest != '\0')
	{
		dest++;
	}
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (c_s1);
}
